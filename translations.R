# Default language
DEFAULT_LANG <- "en"

# Translation lookup with English fallback
tr_get <- function(lang, key) {
  if (is.null(lang) || !(lang %in% names(TR))) lang <- DEFAULT_LANG
  val <- TR[[lang]][[key]]
  if (is.null(val)) val <- TR[[DEFAULT_LANG]][[key]]
  if (is.null(val)) key else val
}
